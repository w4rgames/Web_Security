Le PHP fait partie de ces nouvelles technologies qui, mal employées, permettent aux hackers de pénétrer dans le système d'informations l'utilisant.<br>
Les terrains de jeux sont aussi nombreux que les failles, mais pour se protéger, encore faut-il les connaître...<br><br>

